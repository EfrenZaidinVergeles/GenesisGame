; ******************************************************************
; Sega Addresses Equates
; ******************************************************************

; ******************************************************************
; ROM Cartridge
; ******************************************************************

ROMSTART:   EQU $000000
ROMEND:     EQU $400000

; ******************************************************************
; I/O
; ******************************************************************

IOSTART:    EQU $A00000
IOEND:      EQU $B00000
VERSION:    EQU $A10001


; ******************************************************************
; Z80
; ******************************************************************

Z80BUSREQ:  EQU $A11100     ; Bit 0 of Z80BUSREQ is the STATUS bit, (0=Z80 using the bus, 1=68K can access the bus)
Z80BUSRES:  EQU $A11200

; ******************************************************************
; VDP
; ******************************************************************

VDPSTART:   EQU $C00000
VDPEND:     EQU $E00000

; ******************************************************************
; 68K RAM
; ******************************************************************

WORKRAMSTART:   EQU $FF000000
WORKRAMEND:     EQU $FFFFFF